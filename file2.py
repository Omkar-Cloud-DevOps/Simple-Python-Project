num1 = 10 
num2 = 5 

# Addition
result1 = num1 + num2
print("Addition:", result1)
# Integer Division 
result2 = num1 // num2 
print("Integer Division:", result2) 
# Modulus (Remainder) 
result3 = num1 % num2 
print("Modulus (Remainder):", result3) 
# Absolute Value 
result4 = abs(-7) 
print("Absolute Value:", result4)


